        <h1><li>Introdução</li></h1>
        <p>Os <i>quizzes</i> são uma ótima forma de fazer perguntas sobre diversos conteúdos, semelhante a ele temos os questionários, 
            hoje em dia são poucas vezes utilizados em sala de aula por questões de, às vezes, falta de ferramentas gratuitas de alta qualidade, 
            e nem todos alunos e professores podem ter acesso por questões financeiras. Nesse sentido, o objetivo geral deste trabalho é fazer uma plataforma on-line em que 
            professores possam cadastrar questões sobre assuntos personalizados de sua área de atuação para seus alunos, como forma de estudo. 
            Para isso, será necessário pesquisar trabalhos relacionados ao desenvolvimento de quizzes, na sequência estudar as técnicas e linguagens de programação 
            buscando identificar as mais adequadas para o desenvolvimento do projeto, projetar a interface gráfica onde alunos e professores realizarão 
            a interação, desenvolver a aplicação, elaborar e cadastrar as questões de conteúdos diversos e por fim, testar com estudantes. 
            Espera-se que com este trabalho os <i>quizzes</i> possam ser utilizados na área da educação como uma forma de tornar o aprendizado mais atrativo e agradável.</p>
